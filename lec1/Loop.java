public class Loop {
    public static void main(String[] args) {

        // if condition
        int a = 10;
        if (a == 10) {
            System.out.println("correct");
        }

        // while loop
        int count = 1;
        while(count != 5 ) {
            System.out.println(count);
            count++;

        }

        //for loop

        for(int counting = 1; counting != 5; counting++ ){
            System.out.println(counting);
        }
    }
}
